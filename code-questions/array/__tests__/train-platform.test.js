const { findPlatform } = require('../train-platform');

describe('train-platform tests', () => {
    test.each([
        [
            ['0900', '0940', '0950', '1100', '1500', '1800'],
            ['0910', '1200', '1120', '1130', '1900', '2000'],
            3,
        ],
        [
            ['0900', '0940', '0950', '1100', '1500', '1800', '2000'],
            ['1200', '1200', '1120', '1130', '1900', '2000', '2100'],
            4,
        ],
        [['0900', '1100', '1235'], ['1000', '1200', '1240'], 1],
        [['0900', '1100', '1235'], ['1500', '1200', '1240'], 2],
        [['0500', '0600', '0700'], ['0550', '0650', '0750'], 1],
        [['0500', '0600', '0700'], ['0600', '0700', '0750'], 1],
        [['0400', '0450', '0900', '1500'], ['0850', '1000', '1100', '1700'], 2],
    ])('function accepts arrays:%s & %s, then returns: %s', (arr, dep, res) => {
        expect(findPlatform(arr, dep)).toBe(res);
    });
});
